package com.codenerdz.expensesmanager.activity.spender;

public class Spender
{
    int spenderImageSource;
    String spenderName;
    int spenderID;

    public int getSpenderImageSource() {
        return spenderImageSource;
    }

    public void setSpenderImageSource(int spenderImageSource) {
        this.spenderImageSource = spenderImageSource;
    }

    public String getSpenderName() {
        return spenderName;
    }

    public void setSpenderName(String spenderName) {
        this.spenderName = spenderName;
    }

    public int getSpenderID() {
        return spenderID;
    }

    public void setSpenderID(int spenderID) {
        this.spenderID = spenderID;
    }
}
